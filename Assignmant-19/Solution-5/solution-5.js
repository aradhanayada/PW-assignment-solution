// Use the while loop to print n9mbers from 1 to 25 in ascending and descending order.
// Ascending order
console.log("Ascending Order:");
let numAsc = 1;
while (numAsc <= 25) {
    console.log(numAsc);
    numAsc++;
}

// Descending order
console.log("Descending Order:");
let numDesc = 25;
while (numDesc >= 1) {
    console.log(numDesc);
    numDesc--;
}
