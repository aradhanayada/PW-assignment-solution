// Use a nested ternary operator to check that a number is positive, negative or zero. 
// You have to print “positive” if the number is positive and similarly for negative and zero also.
let number = 5; // You can replace this with any number

let result = (number > 0) ? "positive" : (number < 0) ? "negative" : "zero";

console.log(`The number is ${result}.`);
